
            public class basic138_7024{
                public static float abs(float value) {
                    return value >= 0 ? value : -value;
                }
            
                public static void main(String[] args) {
                    float value = -73.02f;
            
                    float absoluteValue = abs(value);
            
                    System.out.println
                    ("The absolute value of " + value + " is " + absoluteValue);
                }
            }
            
        